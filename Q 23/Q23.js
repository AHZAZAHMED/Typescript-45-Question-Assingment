// Test 1: Equality test
var num1 = 10;
var num2 = 10;
console.log("Is num1 equal to num2? I predict true.");
console.log(num1 == num2);
// Test 2: Inequality test
var str1 = "hello";
var str2 = "world";
console.log("Is str1 not equal to str2? I predict true.");
console.log(str1 = str2);
// Test 3: Greater than test
var x = 5;
var y = 3;
console.log("Is x greater than y? I predict true.");
console.log(x > y);
// Test 4: Less than test
var a = 10;
var b = 20;
console.log("Is b less than a? I predict true.");
console.log(a > b);
// Test 5: Greater than or equal to test
var p = 15;
var q = 10;
console.log("Is p greater than or equal to q? I predict true.");
console.log(p >= q);
// Test 6: Less than or equal to test
var m = 100;
var n = 99;
console.log("Is m less than or equal to n? I predict true.");
console.log(m <= n);
// Test 7: Logical AND test
var sunny = true;
var warm = false;
console.log("Is it sunny and warm? I predict true.");
console.log(sunny && warm);
// Test 8: Logical OR test
var raining = false;
var windy = true;
console.log("Is it raining or windy? I predict true.");
console.log(raining || windy);
// Test 9: Logical NOT test
var isSnowing = false;
console.log("Is it  snowing? I predict true.");
console.log(isSnowing);
// Test 10: String equality test
var fruit1 = "apple";
var fruit2 = "banana";
console.log("Is fruit1 not equal to fruit2? I predict false.");
console.log(fruit1 != fruit2);
